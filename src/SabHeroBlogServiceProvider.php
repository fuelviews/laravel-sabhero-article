<?php

namespace Fuelviews\SabHeroBlog;

use Fuelviews\SabHeroBlog\Components\Breadcrumb;
use Fuelviews\SabHeroBlog\Components\Card;
use Fuelviews\SabHeroBlog\Components\FeatureCard;
use Fuelviews\SabHeroBlog\Components\HeaderCategory;
use Fuelviews\SabHeroBlog\Components\Layout;
use Fuelviews\SabHeroBlog\Components\Markdown;
use Fuelviews\SabHeroBlog\Components\RecentPost;
use Fuelviews\SabHeroBlog\Http\Livewire\SearchAutocomplete;
use Fuelviews\SabHeroBlog\Models\Post;
use Illuminate\Pagination\Paginator;
use Illuminate\Support\Facades\Route;
use Illuminate\Support\Facades\View;
use Livewire\Livewire;
use Spatie\Feed\FeedServiceProvider;
use Spatie\LaravelPackageTools\Package;
use Spatie\LaravelPackageTools\PackageServiceProvider;

class SabHeroBlogServiceProvider extends PackageServiceProvider
{
    public function configurePackage(Package $package): void
    {
        $package->name('sabhero-blog')
            ->hasConfigFile([
                'sabhero-blog',
                'feed',
            ])
            ->hasMigrations([
                'add_author_fields_to_users_table',
                'create_blog_tables',
                'create_media_table',
                'create_imports_table',
                'create_exports_table',
                'create_failed_import_rows_table',
            ])
            ->hasViewComponents(
                'sabhero-blog',
                Layout::class,
                RecentPost::class,
                HeaderCategory::class,
                FeatureCard::class,
                Card::class,
                Markdown::class,
                Breadcrumb::class,
            )
            ->hasViews('sabhero-blog')
            ->hasRoutes([
                'web',
                'breadcrumbs',
            ]);
        // $this->loadTestingMigration();
    }

    public function register()
    {
        Paginator::defaultView('sabhero-blog::pagination.tailwind');

        Route::bind('post', function ($value) {
            return Post::where('slug', $value)
                ->published()
                ->with(['user', 'categories', 'tags', 'media'])
                ->firstOrFail();
        });

        Route::bind('user', function ($value) {
            $userModel = config('sabhero-blog.user.model');

            return $userModel::where('slug', $value)->firstOrFail();
        });

        View::composer([
            '*',
        ], static function ($view) {
            if (request()->route() &&
                in_array(request()->route()->getName(), [
                    'sabhero-blog.post.show',
                    'sabhero-blog.post.metro.show',
                ])) {
                $seoPost = request()->route('post');

                $view->with([
                    'seoPost' => $seoPost,
                ]);
            }
        });

        // Register just the feed view
        View::addNamespace('sabhero-blog', __DIR__.'/../resources/views');

        $this->app->register(FeedServiceProvider::class);

        $this->app->register(SabHeroBlogEventServiceProvider::class);

        return parent::register(); // TODO: Change the autogenerated stub
    }

    public function packageBooted(): void
    {
        // Register Livewire components if Livewire is available and not in testing
        if (class_exists(\Livewire\Livewire::class) && !$this->app->environment('testing')) {
            Livewire::component('sabhero-blog::search-autocomplete', SearchAutocomplete::class);
        }
    }

    public function loadTestingMigration(): void
    {
        if ($this->app->environment('testing')) {
            $this->loadMigrationsFrom(__DIR__.'/../database/migrations');
        }
    }
}
